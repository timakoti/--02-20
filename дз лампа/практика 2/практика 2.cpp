#include <iostream>
using namespace std;

int main()
{
    int a; char b; char c;
    cout << "time of day?" << endl;
    cin >> a;
    cout << "curtains closed?" << endl;
    cin >> b;
    cout << "lamp on ?"<<endl;
    cin >> c;
    if (a >= 7 && a <= 18) {
        if (b == '+') {
            if (c == '+') {
                cout << "light";
            }
            else {
                cout << "dark";
            }
        }
        else {
            cout << "light";
        }
    }
    else {
        if (c == '+') {
            cout << "light";
        }
        else {
            cout << "dark";
        }
    }
}

